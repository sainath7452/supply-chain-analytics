-- 3a. STG row counts (these feed the dashboard)
SELECT 'STG.DIM_SUPPLIER'         obj, COUNT(*) n FROM STG.DIM_SUPPLIER
UNION ALL SELECT 'STG.DIM_PRODUCT',         COUNT(*) FROM STG.DIM_PRODUCT
UNION ALL SELECT 'STG.DIM_CUSTOMER',        COUNT(*) FROM STG.DIM_CUSTOMER
UNION ALL SELECT 'STG.DIM_WAREHOUSE',       COUNT(*) FROM STG.DIM_WAREHOUSE
UNION ALL SELECT 'STG.DIM_SHIP_MODE',       COUNT(*) FROM STG.DIM_SHIP_MODE
UNION ALL SELECT 'STG.DIM_DATE',            COUNT(*) FROM STG.DIM_DATE
UNION ALL SELECT 'STG.FACT_PURCHASE_ORDERS',COUNT(*) FROM STG.FACT_PURCHASE_ORDERS
UNION ALL SELECT 'STG.FACT_SALES_ORDERS',   COUNT(*) FROM STG.FACT_SALES_ORDERS
UNION ALL SELECT 'STG.FACT_INVENTORY',      COUNT(*) FROM STG.FACT_INVENTORY
ORDER BY 1;

-- 3b. What did we quarantine, and why?
SELECT 'PURCHASE_ORDERS' fact, REJECT_REASON, COUNT(*) n FROM REJECT.FACT_PURCHASE_ORDERS GROUP BY ALL
UNION ALL
SELECT 'SALES_ORDERS', REJECT_REASON, COUNT(*) FROM REJECT.FACT_SALES_ORDERS GROUP BY ALL
UNION ALL
SELECT 'INVENTORY', REJECT_REASON, COUNT(*) FROM REJECT.FACT_INVENTORY GROUP BY ALL
ORDER BY 1, 3 DESC;

-- 3c. Proof the cleaning worked: no dirty artifacts remain in STG
SELECT 'orphan supplier keys in product' chk, COUNT(*) n
FROM STG.DIM_PRODUCT WHERE PRIMARY_SUPPLIER_KEY IS NULL          -- expect 3 (set to NULL)
UNION ALL
SELECT 'distinct supplier countries', COUNT(DISTINCT COUNTRY) FROM STG.DIM_SUPPLIER
UNION ALL
SELECT 'distinct product categories', COUNT(DISTINCT CATEGORY) FROM STG.DIM_PRODUCT  -- expect 8
UNION ALL
SELECT 'distinct customer segments', COUNT(DISTINCT SEGMENT) FROM STG.DIM_CUSTOMER   -- expect 4
UNION ALL
SELECT 'PO rows with unparsed order_date', COUNT(*) FROM STG.FACT_PURCHASE_ORDERS WHERE ORDER_DATE IS NULL;
